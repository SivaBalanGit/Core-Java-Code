public class ForEachLoop {

    public static void main(String[] args) {

        int nums[] = new int[10];

        nums[0] = 12;
        nums[1] = 13;
        nums[2] = 14;
        nums[3] = 15;
        nums[4] = 16;
        nums[5] = 17;
        nums[6] = 18;
        nums[7] = 19;
        nums[8] = 20;
        nums[9] = 21;

        for (int n : nums) {

            System.out.println(n);
        }

    }

}
