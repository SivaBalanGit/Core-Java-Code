import java.util.*;

public class Array {

    public static void main(String[] args) {

        int num[] = new int[4];

        num[0] = 23;
        num[1] = 10;
        num[2] = 3;
        num[3] = 56;

        // get all array value

        for (int i = 0; i <= num.length; i++) {
            System.out.println(num[i]);
        }

    }

}
